package com.avinash.training;

public class ForLoop {

	public static void main(String[] args) {
		
		//syntax for(initialization ; condition ; increment/decrement)
		for(int i =0 ; i <5 ; i++)
		
		{
			System.out.println("Avinash");
		}
		
		for(int p =1 ; p <=99 ; p +=2){
			System.out.print(p + ",");
		}
	}

}
